# Propriedades da lista de números

lista = [16, 14, 63, 65, 17, 99, 70, 11, 20, 48, 79, 32, 17, 89, 12, 25, 66]

tam = len(lista)
maior = max(lista)
menor = min(lista)
soma = sum(lista)

print('A lista possui {} números em que o maior número é {} e o menor número é {}. A soma dos valores presentes nela é igual a {}.'.format(tam, maior, menor, soma))
